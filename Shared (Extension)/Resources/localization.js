export const getCurrentLangLabelString = (key) => {
  const langCode = window.navigator.language || 'en';
  const baseLang = langCode.split('-')[0];

  return (
    labelStrings[langCode]?.[key] ??
    labelStrings[baseLang]?.[key] ??
    labelStrings.en[key]
  );
};
